#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	char	*copy;
	int		i;

	i = 0;
	len = 0;
	while (s1[len] != 0)
		len++;
	if (!(copy = (char*)malloc(sizeof(char) * len + 1)))
		return (NULL);
	while (i < len)
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
