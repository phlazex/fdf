#include "libft.h"

void	ft_free_mas(char ***mas)
{
	char	**s;

	s = *mas;
	while (*s)
	{
		if (*s)
		{
			free(*s);
			*s = NULL;
		}
		s++;
	}
	free(*mas);
}
