#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	c = '0';
	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		n = -(n % 1000000000);
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if ((n / 10) > 0)
		ft_putnbr_fd(n / 10, fd);
	c = '0' + (n % 10);
	write(fd, &c, 1);
}
