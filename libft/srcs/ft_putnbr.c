#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	c = '0';
	if (n == -2147483648)
	{
		write(1, "-2", 2);
		n = -(n % 1000000000);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if ((n / 10) > 0)
		ft_putnbr(n / 10);
	c = '0' + (n % 10);
	write(1, &c, 1);
}
