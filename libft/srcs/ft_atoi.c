#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int num;
	int np;

	num = 0;
	i = 0;
	np = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f'
			|| str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
		i++;
	if (str[i - 1] == '-' && i != 0)
		np = -1;
	while (str[i] >= 48 && str[i] <= 57)
	{
		num = (num * 10) + (str[i] - 48);
		i++;
	}
	if (np > 0)
		return (num);
	return (-num);
}
